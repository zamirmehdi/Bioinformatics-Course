def trace_back(strings, x, y):
    directions = direction_matrix[x][y]

    for direction in directions:
        next_cell = ()
        if direction == 'd':
            next_cell = (x - 1, y - 1)
        if direction == 'h':
            next_cell = (x, y - 1)
        if direction == 'v':
            next_cell = (x - 1, y)

        if next_cell[0] == 0 or next_cell[1] == 0:
            string1 = strings[0]
            string2 = strings[1]

            # TODO add gaps to start of strings if needed
            for i in range(x - 2, -1, -1):
                string1 = string1 + str1[i]
                string2 = string2 + '-'
            for i in range(y - 2, -1, -1):
                string1 = string1 + '-'
                string2 = string2 + str2[i]

            string1 = string1[::-1]
            string2 = string2[::-1]

            seq.append((string1, string2))
        else:
            if direction == 'd':
                new_string1 = strings[0] + str1[next_cell[0] - 1]
                new_string2 = strings[1] + str2[next_cell[1] - 1]
                trace_back((new_string1, new_string2), x - 1, y - 1)
            elif direction == 'h':
                new_string1 = strings[0][:-1] + '-' + str1[next_cell[0] - 1]
                new_string2 = strings[1] + str2[next_cell[1] - 1]
                trace_back((new_string1, new_string2), x, y - 1)
            elif direction == 'v':
                new_string1 = strings[0] + str1[next_cell[0] - 1]
                new_string2 = strings[1][:-1] + '-' + str2[next_cell[1] - 1]

                trace_back((new_string1, new_string2), x - 1, y)

    return


def semi_global_alignment():
    for loc in total_locations:
        x, y = loc[0], loc[1]
        string1 = '' + str1[x - 1]
        string2 = '' + str2[y - 1]

        # TODO add gaps to start of strings if needed
        for i in range(x, len(str1)):
            string1 = str1[i] + string1
            string2 = '-' + string2
        for i in range(y, len(str2)):
            string1 = '-' + string1
            string2 = str2[i] + string2

        trace_back((string1, string2), x, y)


# Find cells with total score
def find_total_score_locations():
    global total_locations

    for i in range(len(str1) + 1):
        for j in range(len(str2) + 1):
            if score_matrix[i][j] >= total_score:
                if i == len(str1) or j == len(str2):
                    total_locations.append((i, j))


def calculate_score(arr, i, j):
    temp_direction = ''
    temp_score = 0

    if (not i == 0) and (not j == 0):
        diagonal = arr[i - 1][j - 1] + PAM250[str1[i - 1]][str2[j - 1]]
        horizontal = arr[i][j - 1] + GAP_PENALTY
        vertical = arr[i - 1][j] + GAP_PENALTY

        temp_score = max(diagonal, horizontal, vertical)

        # Find total score in last row and column
        global total_score
        if temp_score >= total_score:
            if i == len(str1) or j == len(str2):
                total_score = temp_score

        if temp_score == diagonal:
            temp_direction += 'd'
        if temp_score == horizontal:
            temp_direction += 'h'
        if temp_score == vertical:
            temp_direction += 'v'

    return temp_score, temp_direction


